                  better understand include the following:
                  <ul>
                    <li>
                      <strong><i>Conversion terms.</i></strong> These are the
                      specific terms by which the amount you invested in the
                      SAFE gets converted into equity. Is it just your original
                      investment or does the SAFE provide for an amount that
                      offers value over time similar to interest on a loan? How
                      does the valuation of the company at the future financing
                      play into how many shares you will get upon conversion of
                      your SAFE?
                    </li>
                    <li>
                      <strong><i>Repurchase rights.</i></strong> Is there a
                      provision in the SAFE that allows the company to
                      repurchase your future right to equity instead of it being
                      converted to equity? Do you have any say into whether your
                      right is repurchased and at what price?
                    </li>
                    <li>
                      <strong><i>Dissolution rights.</i></strong> What happens
                      to your SAFE and the money you invested if the company
                      ends up dissolving?
                    </li>
                    <li>
                      <strong><i>Voting rights.</i></strong> SAFEs do not
                      represent current equity stakes in the company so do not
                      have voting rights similar to common stock. But are there
                      particular circumstances mentioned in the SAFE that allow
                      you a voice on matters pertaining to your SAFE?
                    </li>
                  </ul>
                </li>
                <li>
                  <strong>
                    <i>SAFEs were designed for a specific type of startup.</i>
                  </strong>
                  SAFEs were developed in Silicon Valley as a way for venture
                  capital investors to quickly invest in a hot startup without
                  burdening the startup with the more labored negotiations an
                  equity offering may entail. Oftentimes, for the venture
                  capital investor, it was more important to get the investment
                  opportunity, and possible future opportunities, with the
                  startup than it was to protect the relatively small investment
                  represented by the SAFE. In addition, the various mechanisms
                  of the SAFE, from the triggering events to the conversion
                  terms, were designed to best operate in the context of a fast
                  growing startup likely to need and attract additional capital
                  from sophisticated venture capital investors. This may or may
                  not be the case with the crowdfunding investment opportunity
                  you are exploring.
                </li>
                <li>
                  <strong>
                    <i>There is nothing standard or simple about a SAFE.</i>
                  </strong>
                  Various terms from the triggering events to the conversion
                  price are subject to different treatment by different
                  companies offering SAFEs. It is important to read and
                  understand the company’s disclosure regarding the SAFE it is
                  offering as well as the terms set forth in the actual
                  agreement.
                </li>
              </ul>
