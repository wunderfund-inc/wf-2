                  </strong>
                  Despite the identified triggers for conversion of the SAFE,
                  there may be scenarios in which the triggers are not activated
                  and the SAFE is not converted, leaving you with nothing. For
                  example, if a company in which you invested makes enough money
                  that it never again needs to raise capital, and it is not
                  acquired by another company, then the conversion of the SAFE
                  may never be triggered.
                </li>
              </ul>
