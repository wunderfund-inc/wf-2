              <p>
                It is important to understand the terms of any SAFE in which you
                are investing through a crowdfunding offering. Here are some
                things to keep in mind:
              </p>

              <ul>
                <li>
                  <strong>
                    The most important thing to realize about SAFEs is that you
                    are not getting an equity stake in return. SAFEs are not
                    common stock.
                  </strong>
                  Common stock represents an ownership stake in a company and
                  entitles you to certain rights under state corporate law and
                  federal securities law. A SAFE, on the other hand, is an
                  agreement to provide you a future equity stake based on the
                  amount you invested if—and only if—a triggering event occurs.
                  <strong>
                    <i>
                      SAFEs do not represent a current equity stake in the
                      company in which you are investing. Instead, the terms of
                      the SAFE have to be met in order for you to receive your
                      equity stake.
                    </i>
                  </strong>
                </li>
                <li>
                  <strong>
                    <i>
                      SAFEs may only convert to equity if certain triggering
                      events occur.
                    </i>
                  </strong>
                  Because SAFEs convert to actual equity in the future based on
                  some future event, it is important to understand what exactly
                  triggers the conversion of the SAFE. The terms of the SAFE may
                  have it trigger in a number of different scenarios that may—or
                  may not—occur in the future with respect to the company. For
                  example, a SAFE may be triggered if the company is acquired by
